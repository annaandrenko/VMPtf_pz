def even_numbers(nums): #оголошуємо функцію
    res = [] #оголошуємо список, який буде використовуватись для зберігання парних чисел
    for num in nums: #створюємо цикл, який проходить по списку чисел
        if num % 2 == 0: #перевірка на парність числа
            res.append(num) #якщо число парне, то додаємо до результуючого списку
    return res #повертаємо список парних чисел